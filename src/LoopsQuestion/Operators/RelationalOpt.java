package Operators;

public class RelationalOpt {
    public static void main(String[] args) {
        int p =10, q =15;

        System.out.println(p==q);//False
        System.out.println(p!=q);//true
        System.out.println(p<=q);//True
        System.out.println(p>=q);//False
        System.out.println(p<q);//True
        System.out.println(p>q);//Fslse
    }
}
